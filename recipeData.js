// how to get the recipe name which is the key in the recipes object. 
Object.keys(recipes)[0]

function getRecipe(recipe_name) {
    document.getElementById("recipeName").innerHTML = recipe_name;

    let recipe = recipes[recipe_name];

    let ingredientList = "";
    let instructionsList = "";

    for (var i = 0; i < recipe.ingredients.length; i++) {
        ingredientList += "<li>" + recipe.ingredients[i] + "</li>";
    }

    for (var i = 0; i < recipe.instructions.length; i++) {
        instructionsList += "<li>" + recipe.instructions[i] + "</li>";
    }

    document.getElementById("ingredients").innerHTML = ingredientList;
    document.getElementById("instructions").innerHTML = instructionsList;
}
